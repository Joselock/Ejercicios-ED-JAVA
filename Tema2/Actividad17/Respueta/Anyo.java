public class Anyo {
    private String id;
    private String nombrePP;
    private String nombreJA;
    
    public Anyo(String id, String nombrePP, String nombreJA) {
        this.id = id;
        this.nombrePP = nombrePP;
        this.nombreJA = nombreJA;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombrePP() {
        return nombrePP;
    }

    public void setNombrePP(String nombrePP) {
        this.nombrePP = nombrePP;
    }

    public String getNombreJA() {
        return nombreJA;
    }

    public void setNombreJA(String nombreJA) {
        this.nombreJA = nombreJA;
    }

    

}

