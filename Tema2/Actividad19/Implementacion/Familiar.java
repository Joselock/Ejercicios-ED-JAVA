public class Familiar {
    private String nombre;
    private int edad;
    private int lineaSucecion;

    public Familiar(String nombre, int edad, int lineaSucecion) {
        this.nombre = nombre;
        this.edad = edad;
        this.lineaSucecion = lineaSucecion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getLineaSucecion() {
        return lineaSucecion;
    }

    public void setLineaSucecion(int lineaSucecion) {
        this.lineaSucecion = lineaSucecion;
    }

    
    

}
